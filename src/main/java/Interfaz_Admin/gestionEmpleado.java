    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
       int fila = tblClientes.getSelectedRow();
    if (fila == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un empleado de la tabla.");
        return;
    }

    String nombres = txtCodigo.getText().trim();
    String apePaterno = txtTitulo.getText().trim();
    String apeMaterno = txtAutor.getText().trim();
    String direccion = txtPrecio.getText().trim();
    String telefono = txtCodigo1.getText().trim();
    String correo = txtCodigo4.getText().trim();
    String rol = txtCodigo2.getText().trim();

    if (nombres.isEmpty() || apePaterno.isEmpty() || apeMaterno.isEmpty()
            || direccion.isEmpty() || telefono.isEmpty()
            || correo.isEmpty() || rol.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Complete todos los campos.");
        return;
    }

    Empleado emp = listaEmpleados.get(fila);
    emp.setNombres(nombres);
    emp.setApellidoPaterno(apePaterno);
    emp.setApellidoMaterno(apeMaterno);
    emp.setDireccion(direccion);
    emp.setTelefono(telefono);
    emp.setCorreo(correo);
    emp.setRol(rol);

    cargarTabla();   // ðŸ‘ˆ vuelve a pintarse la tabla
    limpiarCampos();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
     int fila = tblClientes.getSelectedRow();
    if (fila == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un empleado de la tabla.");
        return;
    }

    // Eliminamos de la lista
    listaEmpleados.remove(fila);

    // Volvemos a cargar la tabla desde la lista
    cargarTabla();

    // Limpiamos los campos de texto
    limpiarCampos();
    }//GEN-LAST:event_btnEliminarActionPerformed

