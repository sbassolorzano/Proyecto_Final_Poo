    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed

        
        
        if (txtCodigo.getText().trim().isEmpty() || txtPrecio.getText().trim().isEmpty() ||
            cboCliente.getSelectedItem() == null || cboVendedor.getSelectedItem() == null || 
            cboVehiculo.getSelectedItem() == null) {
            
            JOptionPane.showMessageDialog(this, "Complete todos los datos (incluyendo el precio).");
            return;
        }

        try {
            // 2. Obtener datos
            String codigo = txtCodigo.getText().trim();
            double precioManual = Double.parseDouble(txtPrecio.getText().trim());
            
            Cliente cliente = (Cliente) cboCliente.getSelectedItem();
            Vendedor vendedor = (Vendedor) cboVendedor.getSelectedItem();
            Vehiculo vehiculoOriginal = (Vehiculo) cboVehiculo.getSelectedItem();

            // 3. TRUCO PARA EL PRECIO MANUAL:
            // Como Cotizacion lee el precio del vehículo, creamos una copia temporal del vehículo
            // con el precio que tú escribiste, solo para esta cotización.
            Vehiculo vehiculoCotizado = new Vehiculo(
                    vehiculoOriginal.getCodigo(),
                    vehiculoOriginal.getMarca(),
                    vehiculoOriginal.getModelo(),
                    vehiculoOriginal.getColor(),
                    vehiculoOriginal.getAnioFabricacion(),
                    vehiculoOriginal.getTipo(),
                    precioManual, // <--- ¡AQUÍ VA TU PRECIO MANUAL!
                    vehiculoOriginal.getEstado()
            ) {}; // Las llaves {} son porque Vehiculo es abstracta

            // 4. Crear Cotización (Sin promoción, pasamos null)
            Cotizacion nuevaCoti = new Cotizacion(codigo, cliente, vendedor, vehiculoCotizado, null);

            // 5. Guardar y Actualizar
            GestionReportes1.agregarCotizaciones(nuevaCoti);
            listarCotizaciones();
            
            JOptionPane.showMessageDialog(this, "Cotización guardada con precio: S/ " + precioManual);
            txtCodigo.setText("");
            txtPrecio.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
      
    }//GEN-LAST:event_btnGenerarActionPerformed

