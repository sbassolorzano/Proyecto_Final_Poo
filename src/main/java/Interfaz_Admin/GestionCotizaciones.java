    private void configurarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Cód.");
        modelo.addColumn("Cliente");
        modelo.addColumn("Vehículo");
        modelo.addColumn("Precio Base");
        modelo.addColumn("Descuento");
        modelo.addColumn("Total");
        modelo.addColumn("Estado");
        
        tblCotizaciones.setModel(modelo);
    }
    private void llenarCombos() {
        // Limpiamos primero
        cboCliente.removeAllItems();
        cboVendedor.removeAllItems();
        cboVehiculo.removeAllItems();

        // 1. Clientes
        for (int i = 0; i < GestionReportes1.getContClientes(); i++) {
            cboCliente.addItem(GestionReportes1.getListaClientes()[i]);
        }

        // 2. Vendedores (Solo empleados con rol VENDEDOR)
        for (int i = 0; i < GestionReportes1.getContEmpleados(); i++) {
            Empleado emp = GestionReportes1.getListaEmpleados()[i];
            if (emp.esVendedor()) { 
                cboVendedor.addItem((Vendedor) emp);
            }
        }

        // 3. Vehículos (Solo los DISPONIBLES)
        for (int i = 0; i < GestionReportes1.getContVehiculos(); i++) {
            Vehiculo v = GestionReportes1.getListaVehiculos()[i];
            if (v.getEstado() == EstadoVehiculo.DISPONIBLE) {
                cboVehiculo.addItem(v);
            }
        }  
    }


    private void listarCotizaciones() {
        DefaultTableModel modelo = (DefaultTableModel) tblCotizaciones.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        for (int i = 0; i < GestionReportes1.getContCotizaciones(); i++) {
            Cotizacion c = GestionReportes1.getListaCotizaciones()[i];
            
            Object[] fila = {
                c.getCodigo(),
                c.getCliente().getNombres(),
                c.getVehiculo().getMarca() + " " + c.getVehiculo().getModelo(),
                c.getPrecioBase(),
                c.getDescuento(),
                c.getTotal(),
                c.getEstado()
            };
            modelo.addRow(fila);
        }
    }

        
    
    
    

