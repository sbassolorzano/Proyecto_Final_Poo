private void jbIgresarActionPerformed(java.awt.event.ActionEvent evt) {                                           
    if (txtNombre1.getText().trim().equals("")) {
        mensaje("Ingrese el usuario");
        txtNombre1.requestFocus();
        return;
    }

    // Obtener la contraseña desde el JPasswordField
    char[] passChars = jPasswordField1.getPassword();
    String desClave = new String(passChars);

    if (desClave.trim().equals("")) {
        mensaje("Ingrese la contraseña");
        jPasswordField1.requestFocus();
        return;
    }

    String desUsuario = txtNombre1.getText().trim();

    // 1) Buscar en los empleados registrados
    Empleado empleadoEncontrado = GestionReportes1.buscarEmpleado(desUsuario, desClave);

    if (empleadoEncontrado != null) {
        // Empleado real (ADMIN o VENDEDOR registrado)
        ingresarAlSistema(empleadoEncontrado);

    } else if (desUsuario.equals("admin") && desClave.equals("ad123")) {
        // Super Admin de emergencia
        Personas.Administrador superAdmin = new Personas.Administrador(
            "00000000", "Super", "Admin", "", "", "", "", "admin", "loco"
        );
        ingresarAlSistema(superAdmin);

    } else if (desUsuario.equals("vend") && desClave.equals("12345")) {
        // VENDEDOR HARDCODEADO
        Empleado vendedorDemo = new Empleado(
            "11111111",      // dni
            "Vendedor",      // nombres
            "Demo",          // apellido paterno
            "",              // apellido materno
            "",              // dirección
            "",              // teléfono
            "",              // correo
            "vend",          // usuario
            "12345",         // contraseña
            "VENDEDOR"       // rol  (importante para MenuPrincipal)
        );
        ingresarAlSistema(vendedorDemo);

    } else {
        mensaje("Usuario o contraseña incorrectos.");
    }
    
}
private void ingresarAlSistema(Empleado emp){
    MenuPrincipal menu =new MenuPrincipal(emp);
    menu.setVisible(true);
    this.dispose();
}







